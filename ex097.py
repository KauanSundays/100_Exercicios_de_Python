def escreva(texto):
    tamanho = len(texto) + 4
    print("~" * tamanho)    
    print(f"  {texto}")
    print("~" * tamanho)    

escreva("Gustavo Guanabara")
escreva("Curso de Python no YouTube")
escreva("CeV")

# O código define uma função chamada escreva() que recebe um texto e o exibe entre linhas de til (~) para criar um "quadro".

